function generate_hiragana_sets(){
    let sets = {}
    for(let character in hiragana){
        let data = hiragana[character]
        for(let set of data.sets){
            if(!sets[set]){
                sets[set] = {}
            }
            sets[set][character] = data
        }
    }
    return sets
}

